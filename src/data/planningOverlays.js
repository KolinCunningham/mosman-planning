export function getProjectedGridStressScore(baseScore, scope, options = {}) {
  const uplift = scope?.id === 'high-uplift' ? 7 : scope?.id === 'moderate-uplift' ? 3 : -6
  const unmanaged = options.managedCharging ? 0 : 7
  const exportPressure = options.v2g || options.solar ? 2 : 0
  return Math.min(99, Math.max(45, Math.round(Number(baseScore || 0) + uplift + unmanaged + exportPressure)))
}

